// MACPAC, AUCD, state agency websites. Verified through Q1 2026.
// NOTE: EVV vendors change via competitive rebid; always verify with state MCO.

export interface StateDDRecord {
  /** Full state name */
  state: string;
  /** Two-letter postal abbreviation */
  abbr: string;
  /** State Medicaid program brand name */
  medicaidName: string;
  /** Primary DD agency name */
  ddAgencyName: string;
  /** Primary DD waiver name (HCBS 1915(c) or 1115 equivalent) */
  ddWaiverName: string;
  /** Self-directed / participant-directed program name (null if none statewide) */
  selfDirectedProgram: string | null;
  /** Primary EVV system vendor(s) used for DD/personal care */
  evvVendors: string[];
  /** Is ABA therapy covered under state Medicaid for children? */
  abaMedicaidCoverage: 'yes' | 'yes_epsdt' | 'limited' | 'no';
  /** Notes on ABA coverage (commercial mandate age cap, dollar cap, etc.) */
  abaNotes: string;
  /** State agency website */
  agencyUrl: string;
  /** State agency main phone */
  agencyPhone: string;
}

