export const STATE_DD_LOOKUP: Record<string, StateDDRecord> = Object.fromEntries(
  [...PRIORITY_STATES, ...OTHER_STATES].map((s) => [s.abbr, s]),
);

/** Sorted array of all state records (alphabetical by state name) */
export const ALL_STATE_DD_RECORDS: StateDDRecord[] = [...PRIORITY_STATES, ...OTHER_STATES].sort(
  (a, b) => a.state.localeCompare(b.state),
);

/** Get a single state record by abbreviation (case-insensitive) */
export function getStateDDRecord(abbr: string): StateDDRecord | undefined {
  return STATE_DD_LOOKUP[abbr.toUpperCase()];
}

/** States that have a statewide self-directed / participant-directed program */
export function getStatesWithSelfDirection(): StateDDRecord[] {
  return ALL_STATE_DD_RECORDS.filter((s) => s.selfDirectedProgram !== null);
}

/** States where ABA is explicitly covered under Medicaid (not just EPSDT fallback) */
export function getStatesWithExplicitABAMedicaid(): StateDDRecord[] {
  return ALL_STATE_DD_RECORDS.filter((s) => s.abaMedicaidCoverage === 'yes');
}

/** Priority states list (by abbreviation) — higher-depth data, larger user base */
export const PRIORITY_STATE_ABBRS: string[] = PRIORITY_STATES.map((s) => s.abbr);
