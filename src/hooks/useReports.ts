/**
 * Reports Hook
 * Manages report generation and storage with real PDF output
 */

import { useState, useCallback } from 'react';
import jsPDF from 'jspdf';

// ============================================================================
// Types
// ============================================================================

export interface Report {
  id: string;
  type: ReportType;
  title: string;
  createdAt: Date;
  status: 'pending' | 'generating' | 'ready' | 'error';
  url?: string;
  blob?: Blob;
  childName?: string;
  reportType?: string;
  reportId?: string;
  expiresAt?: string;
  generatedAt?: string;
}

export type ReportType =
  | 'weekly-outcomes'
  | 'monthly-progress'
  | 'provider-packet'
  | 'iep-summary'
  | 'bcba-notes'
  | 'insurance-letter'
  | 'parent'
  | 'iep'
  | 'bcba';

export interface ReportOptions {
  dateRange?: { start: Date; end: Date };
  includeCharts?: boolean;
  includeNotes?: boolean;
  childName?: string;
  parentName?: string;
  customData?: Record<string, unknown>;
}

// ============================================================================
// PDF Generation Utilities
// ============================================================================

const AMINY_BLUE = '#0891b2';
const AMINY_GREEN = '#4E93A8';
const GRAY_600 = '#4B5563';
const GRAY_400 = '#9CA3AF';

function getReportTitle(type: ReportType): string {
  const titles: Partial<Record<ReportType, string>> = {
    'weekly-outcomes': 'Weekly Outcomes Report',
    'monthly-progress': 'Monthly Progress Summary',
    'provider-packet': 'Provider Referral Packet',
    'iep-summary': 'IEP Progress Report',
    'bcba-notes': 'BCBA Clinical Notes',
    'insurance-letter': 'Insurance Documentation',
    'parent': 'Parent Report',
    'iep': 'IEP Report',
    'bcba': 'BCBA Report',
  };
  return titles[type] || 'Progress Report';
}

function generateBasePDF(
  type: ReportType,
  options: ReportOptions
): jsPDF {
  const doc = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4',
  });

  const pageWidth = doc.internal.pageSize.getWidth();
  const margin = 20;
  let yPos = margin;

  const title = getReportTitle(type);
  const childName = options.childName || 'Child';
  const parentName = options.parentName || 'Parent';
  const dateRange = options.dateRange || {
    start: new Date(Date.now() - 7 * 24 * 60 * 60 * 1000),
    end: new Date(),
  };

  const formatDate = (date: Date) =>
    date.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
    });

  // Header
  doc.setFontSize(24);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(AMINY_BLUE);
  doc.text('Aminy', margin, yPos);
  yPos += 8;

  doc.setFontSize(16);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(GRAY_600);
  doc.text(title, margin, yPos);
  yPos += 10;

  // Period and info
  doc.setFontSize(11);
  doc.setTextColor(GRAY_400);
  doc.text(`${formatDate(dateRange.start)} - ${formatDate(dateRange.end)}`, margin, yPos);
  yPos += 6;
  doc.text(`Child: ${childName} | Parent: ${parentName}`, margin, yPos);
  yPos += 12;

  // Watermark
  doc.setFillColor(245, 245, 245);
  doc.roundedRect(margin, yPos, pageWidth - margin * 2, 10, 2, 2, 'F');
  doc.setFontSize(8);
  doc.setTextColor(GRAY_400);
  doc.text(
    `Generated by Aminy on ${new Date().toLocaleDateString()} | Expires in 7 days`,
    margin + 5,
    yPos + 6
  );
  yPos += 18;

  // Content section header
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(AMINY_BLUE);
  doc.text('Summary', margin, yPos);
  yPos += 10;

  // Placeholder content - in production this would use real data
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(GRAY_600);

  const content = getReportContent(type, childName);
  const lines = doc.splitTextToSize(content, pageWidth - margin * 2);
  doc.text(lines, margin, yPos);
  yPos += lines.length * 5 + 10;

  // Footer
  const pageHeight = doc.internal.pageSize.getHeight();
  doc.setDrawColor(GRAY_400);
  doc.line(margin, pageHeight - 25, pageWidth - margin, pageHeight - 25);

  doc.setFontSize(9);
  doc.setTextColor(GRAY_400);
  doc.text(
    'This report is for informational purposes only. Share with your care team.',
    margin,
    pageHeight - 18
  );
  doc.text(
    'Generated by Aminy AI | support@aminy.ai | www.aminy.ai',
    margin,
    pageHeight - 12
  );

  return doc;
}

