export function useReports(_options?: { accessToken?: string }) {
  const [reports, setReports] = useState<Report[]>([]);
  const [isGenerating, setIsGenerating] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const generateReport = useCallback(
    async (type: ReportType, options?: ReportOptions): Promise<Report> => {
      setIsGenerating(true);
      setError(null);

      try {
        // Generate the PDF
        const doc = generateBasePDF(type, options || {});
        const blob = doc.output('blob');
        const url = URL.createObjectURL(blob);

        const newReport: Report = {
          id: `report_${Date.now()}`,
          type,
          title: getReportTitle(type),
          createdAt: new Date(),
          status: 'ready',
          url,
          blob,
          childName: options?.childName,
        };

        setReports((prev) => [...prev, newReport]);
        return newReport;
      } catch (err) {
        const errorMessage = err instanceof Error ? err.message : 'Failed to generate report';
        setError(errorMessage);
        throw err;
      } finally {
        setIsGenerating(false);
      }
    },
    []
  );

  const deleteReport = useCallback((id: string) => {
    setReports((prev) => {
      const report = prev.find((r) => r.id === id);
      if (report?.url) {
        URL.revokeObjectURL(report.url);
      }
      return prev.filter((r) => r.id !== id);
    });
  }, []);

  const downloadReport = useCallback(
    (id: string, filename?: string) => {
      const report = reports.find((r) => r.id === id);
      if (report?.blob) {
        const url = URL.createObjectURL(report.blob);
        const link = document.createElement('a');
        link.href = url;
        link.download =
          filename ||
          `aminy-${report.type}-${report.childName?.replace(/\s+/g, '-').toLowerCase() || 'report'}-${new Date().toISOString().split('T')[0]}.pdf`;
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
        URL.revokeObjectURL(url);
      } else if (report?.url) {
        window.open(report.url, '_blank');
      }
    },
    [reports]
  );

  const getReportsByType = useCallback(
    (type: ReportType) => {
      return reports.filter((r) => r.type === type);
    },
    [reports]
  );

  const clearReports = useCallback(() => {
    reports.forEach((report) => {
      if (report.url) {
        URL.revokeObjectURL(report.url);
      }
    });
    setReports([]);
  }, [reports]);

  // Aliases for backward compatibility
  const currentReport = reports.length > 0 ? reports[reports.length - 1] : null;
  const loading = isGenerating;
  const createReport = generateReport;
  const fetchReports = (_childId?: string) => { /* no-op, reports loaded from state */ };
  const removeReport = deleteReport;
  const shareReportWithProvider = async (_reportId: string, _email: string, _message?: string) => { /* no-op placeholder */ };

  return {
    reports,
    isGenerating,
    error,
    generateReport,
    deleteReport,
    downloadReport,
    getReportsByType,
    clearReports,
    // Additional aliases used by ReportsHub
    currentReport,
    loading,
    createReport,
    fetchReports,
    removeReport,
    shareReportWithProvider,
  };
}
